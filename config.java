    public static Connection configDB() throws SQLException{
        try {
            String url= "jdbc:mysql://localhost:3306/mahasiswa"; //url database
            String user="root"; //user database
            String pass=""; //password database
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            mysqlconfig=DriverManager.getConnection(url, user, pass);
        } catch (Exception e) {
            System.err.println("koneksi gagal"+e.getMessage()); //perintah untuk menampilkan error pada koneksi
        }
    
    return mysqlconfig;
    }
    
    private void load_table(){
        //membuat tampilan model tabel
       DefaultTableModel model = new DefaultTableModel ();
       model.addColumn("NO");
       model.addColumn("Nama");
       model.addColumn("NIM");
       model.addColumn("Jurusan");
       model.addColumn("Alamat");
       model.addColumn("Phone");
       
       // menampilkan database ke dalam tabel
        try {
            int no=1;
            String sql = " select * from mhs";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm= conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
        }
    }
    
